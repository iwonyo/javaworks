package ch01;

public class PrintData {

	public static void main(String[] args) {
		System.out.println(100);
		System.out.println(3.3); // 숫자
		System.out.println(10 + 100); // 숫자 더하기
		System.out.println("=================");
		
		System.out.println('A'); // 문자 - 홑따움표
		System.out.println("Banana"); // 문자열 - 쌍따움표
		System.out.println(100 + "10"); // 문자 더하기
		System.out.println("=================");
		
		// 불리언 (boolean)
		System.out.println(true);
		System.out.println(4 > 5);
		

	}

}
